    private void jTextField_SearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_SearchKeyReleased
        // TODO add your handling code here:
        ArrayList<Teacher_Bean> al = null;
        al = new ArrayList<Teacher_Bean>();
        String val = jTextField_Search.getText().toString();
        
        try{
        Connection conn = getMysqlConnection();
        String qry = "select * from teachers where name like '%"+val+"%'";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(qry);
        Teacher_Bean teacher;
        while(rs.next()){
    teacher = new Teacher_Bean(Integer.parseInt(rs.getString(1)),rs.getString("name"),Float.parseFloat(rs.getString(3)),rs.getString(4),rs.getString("address"),rs.getString(6));
    al.add(teacher);
        }
        if(st != null){
        st.close();
        }
        if(conn != null){
        conn.close();
        }
         DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
    model.setRowCount(0);
    Object[] row = new Object[6];
    for(int i = 0; i< al.size();i++){
    row[0]=al.get(i).getId();
    row[1]=al.get(i).getName();
    row[2]=al.get(i).getSalary();
    row[3]=al.get(i).getDob();
    row[4]=al.get(i).getAddress();
    row[5]=al.get(i).getSubject();
    model.addRow(row);
    }
     
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Error in Search " +e);
        }
    }//GEN-LAST:event_jTextField_SearchKeyReleased

