    private void jButton2_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_saveActionPerformed
        // TODO add your handling code here:
         if(jTextField_id.getText().toString().equals("")|| jTextField_name.getText().toString().equals("")
                 ||jTextField_salary.getText().toString().equals("")||jTextAreaAddress.getText().toString().equals("")
                 ||jTextFieldSubject.getText().toString().equals("")){
        JOptionPane.showMessageDialog(null,"All Field Are Compulsory");
        }else{
             try{
             Connection conn = getMysqlConnection();
             String qry = "insert into teachers(id,name,salary,dob,address,subject) values(?,?,?,?,?,?)";
             PreparedStatement ps = conn.prepareStatement(qry);
             
             ps.setInt(1,Integer.parseInt(jTextField_id.getText().toString()));
             ps.setString(2,jTextField_name.getText().toString());
             ps.setFloat(3,Float.parseFloat(jTextField_salary.getText().toString()));
             
             SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
             String dob1 = sdf.format(jDateChooserDOB.getDate());
             ps.setString(4,dob1);
             
             ps.setString(5,jTextAreaAddress.getText().toString());
             ps.setString(6,jTextFieldSubject.getText().toString());
             
             int result = ps.executeUpdate();
              FillTable();
             if(result >= 1){
              JOptionPane.showMessageDialog(null,result+"Teacher Record Save Successfully....");
             }else{
                 JOptionPane.showMessageDialog(null,result+"Teacher Record Fail Try Again....");
             }
              if(ps != null){
                  ps.close();
        }
             if(conn!=null){
             conn.close();
             }
             }catch(Exception e){
                 JOptionPane.showMessageDialog(null,"Eorro in Insert"+e);
                 
             }
             //clear the field
        jTextField_id.setText("");
        jTextField_name.setText("");
        jTextField_salary.setText("");
        jTextAreaAddress.setText("");
        jTextFieldSubject.setText("");
        }
        
    }//GEN-LAST:event_jButton2_saveActionPerformed

