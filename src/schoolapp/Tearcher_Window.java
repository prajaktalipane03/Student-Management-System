    private void jButton4_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4_deleteActionPerformed
        // TODO add your handling code here:
        if(jTextField_id.getText().toString().equals("")){
        JOptionPane.showMessageDialog(null,"All Field Are Compulsory");
        }else{
             try{
             Connection conn = getMysqlConnection();
             String qry = "delete from teachers where id = ?";
             PreparedStatement ps = conn.prepareStatement(qry);
             
             ps.setInt(1,Integer.parseInt(jTextField_id.getText().toString()));
             
             int result = ps.executeUpdate();
              FillTable();
             if(result >= 1){
              JOptionPane.showMessageDialog(null,result+"Teacher Record Deleted Successfully "+jTextField_id.getText().toString());
             }else{
                 JOptionPane.showMessageDialog(null,result+"Teacher Record Fail to Delete Try Again....");
             }
              if(ps != null){
                  ps.close();
        }
             if(conn!=null){
             conn.close();
             }
             
             }catch(Exception e){
                 JOptionPane.showMessageDialog(null," error in Delete"+e);
                 
             }
             //clear the field
        jTextField_id.setText("");
        jTextField_name.setText("");
        jTextField_salary.setText("");
        jTextAreaAddress.setText("");
        jTextFieldSubject.setText("");
        }
    }//GEN-LAST:event_jButton4_deleteActionPerformed

